				<h2>360</h2>

				<p>La acción de mover el joystick desde la posición hacia adelante en círculo hacia abajo, atrás, arriba y adelante otra vez en un giro de 360 grados.</p>

				<h2>50/50 (fifty-fifty)</h2>

				<p>Es un tipo de mixup que otorga al jugador 2 opciones ofensivas, las cuales no pueden ser defendidas las dos al mismo tiempo, dando al jugador un chance de 50% de acertar en caso de ser ejecutado apropiadamente.</p>

				<h2>Abare</h2>

				<p>Proviene del verbo japonés abareru, que significa "enfadarse violentamente" jugando agresivamente. Usualmente intentando lanzar pokes mientras se es presionado, en wake up, o enmedio de ataques del oponente.</p>

				<h2>Absolute Block</h2>

				<p>Un tipo de mecanismo de bloqueo en un juego de peleas donde una vez que bloqueas una cadena de hits, soltar la guardia no hará que dejes de bloquear hasta que los hits terminen.</p>

				<h2>Activation</h2>

				<p>Término utilizado cuando entras en modo Dark Force en Vampire Savior, recientemente se refiere a la activación del V-Trigger en Street Fighter V.</p>

				<h2>Advanced Blocking</h2>

				<p>Es un bloqueo que fue implementado para mejorar un bloqueo normal. Este tipo de bloqueo generalmente tiene una buena cantidad de ventajas sobre uno normal, al costo de ser mas dificil de ejecutar o teniendo un algun tipo de costo.</p>

				<p>El primer juego en usar este concepto fue World Heroes 2 de SNK. Notables de mencionar serian el parry en Street Fighter III series, el Just Defend de Garou: Mark of the Wolves, el Guard Impact de Soul Calibur, y el Fautless Defense e Instant Block en la serie de Guilty Gear.</p>

				<h2>Anti-Air</h2>

				<p>Un ataque hecho desde el piso con el propósito de atacar a un oponente que está en el aire. Movimientos de este tipo generalmente se benefician de alta prioridad o tienen invencibilidad en la parte superior del cuerpo. Por ejemplo, en Guilty Gear los golpes Adelante + P.</p>

				<h2>Auto Combo</h2>

				<p>Una secuencia de golpes y movimientos que se ejecuta automáticamente al presionar 1 boton.</p>

				<h2>Auto Guard</h2>

				<p>Interesante característica en varios personajes de juegos de peleas, estos movimientos tienen una animacion especifica, donde cualquier movimiento que entre en contacto con el personaje es bloqueado automáticamente, usualmente anulando cualquier movimiento que entra en contacto con el mientras dure.</p>

				<h2>Baiting</h2>

				<p>El Baiting consiste en realizar movimientos para intentar y provocar una cierta reacción o movimiento de un oponente, para entonces castigar esa reacción o movimiento.</p>

				<h2>Balance</h2>

				<p>Un término utilizado para describir el conjunto de personajes de un juego determinado, específicamente refiriéndose a los personajes que son o no son naturalmente mas fuertes o debiles que otros, "bien balanceado" o "buen balance" se refiere a cuando la mayoria, si no todos, los personajes de un juego estan al par entre ellos.</p>

				<h2>Blockstop</h2>

				<p>La duración en la que no es posible cambiar tu guardia mientras estas bloqueando un ataque. Usualmente tambien es la ventana de tiempo en que tienes oportunidad de realizar un tech hit o Guard Cancel (en Vampire Savior), y es un tiempo previo a entrar en blockstun.</p>

				<h2>Blockstring</h2>

				<p>Un blockstring es una cadena de ataques que son bloqueados. El oponente es forzado a bloquear mientras dure la cadena de golpes, o de otra forma ser golpeado. Un blockstring puede ser entendido como un combo que es bloqueado, una vez que se quita la guardia de un blockstring se convierte en combo.</p>

				<h2>Block Stun</h2>

				<p>El término block stun es usado para referir al tiempo en que un jugador puede realizar un ataque o moverse despues de haber bloqueado.</p>

				<h2>Bread and Butter (BnB)</h2>

				<p>Refiere a un combo o técnica "pan y mantequilla" de un personaje, en el sentido de ser facil y muy completo. Comunmente es un combo o técnica que su uso es obligatorio y altamente recomendado debido a su eficiencia, facilidad de ejecución y versatilidad.</p>

				<h2>Broken (roto)</h2>

				<p>Se refiere a personajes o movimientos que son muy poderosos/ventajosos, tanto que ningun personaje tiene posibilidad de igualarle, tambien es comun escuchar el término "OP", abreviación de "overpowered" o en español "super poderoso".</p>

				<h2>Button Mashing</h2>

				<p>Es un término que se refiere a presionar botones repetidamente de forma aleatoria. Es utilizado tambien para describir la manera en que jugadores inexpertos juegan juegos de peleas.</p>

				<h2>Cancel</h2>

				<p>Cancelar, un movimiento inventado en las series de Street Fighter II, es definido como romper la animación de un movimiento realizando otro movimiento que cancela la terminación del anterior.</p>

				<h2>Chain Combo</h2>

				<p>Es una cadena de golpes que se enlazan simplemente realizando uno despues de otro, resultando en una secuencia sin ningun tipo de dificultad, ejemplo, gatling combos en Guilty Gear Series.</p>

				<h2>Cheap / Cheese (No confundir con CHIP)</h2>

				<p>Ser cheap o cheesy es un término asignado a tacticas ventajosas o repetitivas, o un jugador que usa estos movimientos de una forma "barata" o sin contemplación alguna.</p>

				<h2>Chicken Blocking</h2>

				<p>Utilizado para describir cuando decides bloquear un ataque en el aire, en vez de bloquear en el suelo, esto para evitar lidiar con mixups en los que tienes que decidir entre bloquear arriba o abajo, puesto que en el aire no existe esta condición.</p>

				<h2>Chip Damage (tambien Block Damage)</h2>

				<p>Es el daño reducido de ataques que recibe un personaje mientras bloquea.</p>

				<h2>Combo</h2>

				<p>Cadena de ataques que no pueden ser bloqueados si el primer hit conecta.</p>

				<h2>Command Grab</h2>

				<p>Un command grab (o command throw) difiere de los agarres normales, un agarre normal puede ser rechazado lo cual significa que puedes escapar de ser agarrado presionando varias secuencias de botones. Agarres de comando (command grabs)  no pueden ser rechazados al conectar y comunmente hacen mas daño o permiten realizar un combo despues de conectarlo, requieren una secuencia mas compleja para realizarlos.</p>

				<h2>Counter Hit</h2>

				<p>Se refiere a un termino para un ataque que conecta mientras otro jugador esta en el proceso de realizar un ataque. En la mayoria de los juegos de pelea este tipo de ataques garantizan daño adicional, y/o efectos adicionales como mareo.</p>

				<h2>Counterpick</h2>

				<p>Cuando eliges un personaje con una ventaja estadística sobre el personaje de tu oponente.</p>

				<h2>Criticals</h2>

				<p>Son movimientos que pueden causar mas daño de lo normal, resultando en daño crítico. Ocurren normalmente aleatoriamente. Por ejemplo Shingo Yabuki en King of Fighters.</p>

				<h2>Cross-Up</h2>

				<p>Un cross-up es una situación en donde es mas dificil para tu oponente determinar donde tiene que bloquear izquierda o derecha, comunmente al atacar mientras saltas por encima.</p>

				<h2>Damage Scaling (proration)</h2>

				<p>Refiere a la mecanica en algunos juegos de peleas, donde los ataques pueden hacer menos daño de lo normal por determinada razon. Esta reducción de daño puede resultar del numero de hits en un combo, un movimiento especifico de inicio (Guilty Gear), la cantidad de daño que fue hecho a traves del combo (Last Blade), el tipo de movimiento (Third Strike), numero de usos del ataque u otros factores.</p>

