				<h2>Dragon Punch (DP)</h2>

				<p>Es utilizado para describir un comando que se realiza moviendo el joystick hacia adelante, abajo y diagonal-enfrente, el clasico uppercut de Ryu y Ken en Street Fighter, tambien llamado shoryuken.</p>

				<h2>EX Moves</h2>

				<p>Son versiones mejoradas en daño o caracteristicas de un movimiento especial.</p>

				<h2>Fierce</h2>

				<p>En Street Fighter es un puño fuerte.</p>

				<h2>Footsies</h2>

				<p>Es un modismo de la vieja escuela sobre el juego de rango-medio, terrestre dentro de la estrategia de un juego de peleas. Es cuando ambos jugadores estan fuera del rango de combos y se atacan entre ellos con golpes de rango medio, generalmente ataques seguros.</p>

				<h2>Frame(s)</h2>

				<p>Un frame es un cuadro individual en la imagen que muestra una pantalla. Los juegos de pelea generalmente corren a fijos 60 frames por segundo (50 frames en Europa) lo que significa que muestran 60 imagenes cada segundo para simular el movimiento, lo que nos permite medir la duración o tiempo en que se inicia/recupera cierto movimiento.</p>

				<h2>Frame Advantage</h2>

				<p>Un movimiento que permite al jugador recuperarse antes de que su oponente deje ambos hit stun o block stun, se considera que tienes una "ventaja de frames" con respecto al otro jugador.</p>

				<h2>Frame Data</h2>

				<p>Son los datos de ataques especificos. Estos datos pueden encontrarse en linea y al ser estudiados pueden representar una ventaja para alguien que quiere mejorar su juego. Los datos incluyen: startup o "inicio" (cuantos frames tarda un movimiento en ser activo y golpear al oponente), active frames "frames activos" (la cantidad de frames que un ataque activo puede golpear al oponente), recovery frames "frames de recuperación" (cuantos frames deben pasar para que el jugador vuelva a posición neutral), total frames, frame advantaje(ventaja) o disadvantage (desventaja), un ataque puede ser "+10 on block" significando que si hiciste un ataque y tu oponente bloqueó, tienes una ventaja de 10 frames sobre tu oponente, y viceversa -10 on block, tienes 10 frames de recuperación en los que el oponente puede tomar ventaja.</p>

				<h2>Frame Trap</h2>

				<p>Es una táctica en la cual utilizas un movimiento que parece ser castigable pero realmente tiene ventaja al ser bloqueado, engañando al oponente siendo este castigado al intentar iniciar un ataque.</p>

				<h2>Grappler</h2>

				<p>Grapplers son personajes que su forma de jugar gira entorno al uso de command throws muy fuertes.</p>

				<h2>Guard Break</h2>

				<p>Es cuando un ataque tiene la propiedad de romper la defensa, permitiendote iniciar un combo, o tambien es el momento en que el jugador se queda sin barra de guardia y al continuar reciibiendo ataques esta se rompe.</p>

				<h2>Guard Cancel</h2>

				<p>La acción de cancelar el block stun con otro movimiento para contratacar (en KOF el CD attack, en Guilty Gear el dead angle attack, en SFV el V-Reversal) esto con el fin de deshacerse de la preión del oponente.</p>

				<h2>Guard Meter</h2>

				<p>Es la barra que disminuye al bloquear ataques.</p>

				<h2>Hit Box</h2>

				<p>Es el espacio virtual no visible en el que el jugador tiene alcance dentro de sus ataques, es la zona donde el juego detecta la colisión del un movimiento.</p>

				<h2>Hit Confirm</h2>

				<p>La acción de confirmar o conectar un ataque en un combo o special attack, sobre todo en ciertas situaciones en que el timing o la distancia lo hacen dificil de continuar.</p>

				<h2>Hit Stun</h2>

				<p>Término que se utiliza para referirse a la cantidad de frames que toma recuperarse despues de ser golpeado por determinado ataque.</p>

				<h2>Hood Flawless</h2>

				<p>Ganar una pelea solamente recibiendo daño chip.</p>

				<h2>Infinite (infinito)</h2>

				<p>Un combo que puede continuar indefinidamente.</p>

				<h2>Invincibility (invencibilidad)</h2>

				<p>Ciertos ataques tienen invencibilidad a otros ataques en su duración.</p>

				<h2>Jab</h2>

				<p>En Street Fighter series es un puño débil, en otros juegos cualquier golpe rápido.</p>

				<h2>Juggle</h2>

				<p>Un combo en el cual el oponente es golpeado en multiples ocasiones mientras está en el aire.</p>

				<h2>Kara Cancel</h2>

				<p>Es un tipo especial de cancelación en la que se interrumpen los primeros frames de la animación de algun movimiento para permitir realizar links en donde normalmente no es posible, algunos ataques normales que tienen desplazamiento permiten otorgar rango al movimiento con el que se cancela.</p>

				<h2>Link</h2>

				<p>Es la acción de encadenar movimientos inmediatamente despues de su recuperación para realizar combos complejos.</p>

				<h2>Matchup</h2>

				<p>Refiere a cuando 2 personajes se enfrentan uno al otro en una pelea. Puede ser determinado buen o mal matchup dependiendo de una variedad de factores del personaje como la velocidad, fortaleza, o rango, estos datos son recopilados de acuerdo a los resultados obtenidos en torneos y dan una pauta para conocer que personajes tienen ventaja contra otros.</p>

				<h2>Meaty</h2>

				<p>Utilizado para describir la acción de atacar a un oponente en el momento que este se levanta despues de ser derrivado, realizando el ataque de manera que conecten los ultimos frames activos, de esta forma se conserva el hit/block stun que tiene un ataque, beneficiandose de una recuperación mas rapida.</p>

				<h2>Meter</h2>

				<p>Es la barra que se incrementa en el transcurso del juego, esta barra es utilizada para realizar supers o movimientos EX.</p>

				<h2>Mind Games</h2>

				<p>Se refiere a la psicología utilizada con el fin de construir una estrategia que te permita condicionar las reacciones del oponente y de esta manera "engañar" su percepción de tus movimientos.</p>

				<h2>Mirror Match</h2>

				<p>Una pelea en la que los 2 jugadores utilizan el/los mismo(s) personajes.</p>

				<h2>Mixup</h2>

				<p>Es la estrategia o técnica de hacer los ataques mas dificil de predecir, realizando una "combinación" de ataques por abajo, overhead, o agarres.</p>

				<h2>Negative Edge</h2>

				<p>El uso de soltar un boton presionado en lugar de presionar un boton junto a una secuencia de comandos para realizar un movimiento.</p>

				<h2>Normal move</h2>

				<p>Son ataques que se realizan presionando un solo boton, sin mover el joystick y usualmente sin estar en el aire.</p>

				<h2>OCV</h2>

				<p>Abreviación de "One Character Victory", sucede cuando un jugador gana una pelea en juegos basados en equipos con 1 solo personaje.</p>

				<h2>On the Ground (OTG)</h2>

				<p>Estado en que el personaje fue derrivado al suelo. Un ataque OTG es un ataque que golpea un oponente que se encuentra en el suelo.</p>

				<h2>Okizeme</h2>

				<p>Okizeme (literalmente "ataque al levantarse") es el arte de poner presión sobre un oponente que se esta levantando o derrivado en el suelo.</p>

				<h2>Option Select</h2>

				<p>Describe la situación en un juego de peleas donde la acción de un jugador es ambigua, y la computadora determinará lo consecuente basado en la situación. Generalmente, el resultado elegido es la mejor para el personaje que lo realiza.</p>

				<h2>Overhead Attack</h2>

				<p>Es un ataque que golpea a personajes cuando estan bloqueando agachado y tiene que ser bloqueado parado.</p>

				<h2>Parry</h2>

				<p>Técnica que significa parar un ataque sin recibir daño, al mometo exacto en que el oponente ataca.</p>

				<h2>Plink</h2>

				<p>Plinking o Priority linking es una técnica en donde 2 o mas botones son presionados en una secuencia rápida, permitiendo comandos mas sencillos y en general mejor ejecución.</p>

				<h2>Poke</h2>

				<p>Un poke es generalmente un ataque rápido que es lanzado para golpear al oponente desde medio-maximo rango.</p>

				<h2>Priority(prioridad)</h2>

				<p>Se usa para describir la tendencia de un ataque para ganar el choque en contra de otros.</p>

				<h2>Punish (castigar)</h2>

				<p>Como su nombre lo dice, castigar movimientos erróneos o inseguros, aprovechar un error del adversario.</p>

				<h2>Read/Reading</h2>

				<p>Realizar una lectura de la estrategia o predecir el siguiente movimiento del oponente.</p>

				<h2>Recovery</h2>

				<p>El tiempo que toma a un personaje en volver a estado neutral despues de realizar cualquier movimiento.</p>

				<h2>Rekka</h2>

				<p>Es un tipo de movimiento especial que puede combear repitiendo su propio comando, a un máximo numero de ataques (comunmente 3). Por ejemplo el 236+P repetido 3 veces de Fei-Long nombrado "Rekkaken" en Street Fighter II.</p>

				<h2>Reversal</h2>

				<p>Un movimiento que es hecho en el momento que se favorece de su invencibilidad o prioridad, la ventana de frames para estos movimientos es generalmente chica. Un ejemplo de esto es en Street Fighter, donde un movimiento es realizado en el primer frame posible despues de levantarse, se considera que tiene un "timing de reversal", el Dragon Punch es un movimiento popular usado como reversal.</p>

				<h2>Roundhouse</h2>

				<p>En Street Fighter es una patada fuerte.</p>

				<h2>Rushdown</h2>

				<p>El opuesto de turtling, el estilo rushdown es considerado completamente ofensivo, regularmente usando una gran variedad de mixups, presión y mind games para guiar al oponente dentro de una situación defensiva.</p>

				<h2>Safe</h2>

				<p>Un movimiento que no puede ser castigado al ser bloqueado.</p>

				<h2>Safe Jump</h2>

				<p>Es la técnica de crear el timing en un ataque aéreo contra un oponente derrivado de manera que si intentan hacer un reversal al levantarse, puedas caer al suelo y bloquear con éxito, y si no hace reversal el ataque golpeará al oponente bloqueando, logrando continuar la presión de forma segura.</p>

				<h2>Shenanigans</h2>

				<p>Un truco audaz utilizado para un propósito cuestionable.</p>

				<h2>Short</h2>

				<p>En Street Fighter una patada débil.</p>

				<h2>Shoto</h2>

				<p>Personajes que basan su estrategia en lanzar fireballs (bolas de fuego) o proyectiles de cualquier tipo, por ejemplo, Ryu, Ken y Akuma en Street Fighter.</p>

				<h2>Spam</h2>

				<p>Spammear es basicamente repetir el mismo movimiento una y otra vez, principalmente proyectiles para dominar el espacio en el escenario, dichos movimientos suelen ser seguros o dificiles de castigar.</p>

				<h2>Special Move</h2>

				<p>Son movimientos realizados con diferentes combinaciones de mover joystick y/o presionar botones.</p>

				<h2>Startup</h2>

				<p>El tiempo en frames que toma al movimiento de un personaje en ser activo, entre mas rápido, mejor.</p>

				<h2>Strong</h2>

				<p>En Street Fighter un puño medio.</p>

				<h2>Stun</h2>

				<p>Estado en el que se entra el oponente al recibir demasiado daño, sin permitir bloquear o contratacar mientras dure.</p>

				<h2>Sweep</h2>

				<p>Se refiere a algun golpe normal que derriva al oponente al suelo, la mayoria son patada fuerte agachado.</p>

				<h2>Taunt</h2>

				<p>El saludo o "burla" que tienen algunos personajes.</p>

				<h2>Teching</h2>

				<p>Es cuando escapas de un intento de agarre del oponente.</p>

				<h2>Throws</h2>

				<p>Son agarres que normalmente quitan la defensa.</p>

				<h2>Tier</h2>

				<p>Es una escala en la que se posicionan los personajes de determinado juego, siendo los "Top Tier" considerados como los mas fuertes, "Mid-Tier" los regulares, hasta los "Low-Tier" considerados los que tienen mayor cantidad de debilidades con respecto a los demás, una lista de tiers no siempre refleja la fortaleza de un personaje, ya que los juegos de pelea se basan en gran medida de la habilidad del jugador.</p>

				<h2>Tiger Knee (TK)</h2>

				<p>Ciertos movimientos aéreos que son realizados muy cerca del suelo, es comunmente logrado haciendo el comando + presionando arriba, permitiendo que el movimiento salga tan pronto el personaje deje el suelo.Originalmente fue un glitch, una adición intencional en los nuevos juegos.</p>

				<h2>Trade</h2>

				<p>Es el intercambio de 2 golpes que se ejecutan al mismo tiempo.</p>

				<h2>Turtling</h2>

				<p>El estilo de juego de permanecer en instancia defensiva la mayoria del tiempo de la pelea.</p>

				<h2>Unblockables (inbloqueables)</h2>

				<p>Movimientos que no pueden ser bloqueados de forma normal.</p>

				<h2>Vortex</h2>

				<p>Es una situación en la que un jugador continuamente puede forzar a otro a hacer una predicción desfavorable despues de un combo o setup, resultando en que el oponente sea capturado en el mismo combo una y otra vez.</p>

				<h2>Yomi</h2>

				<p>Yomi (literalmente "leer") es un término japones que significa "leer la mente del oponente", y es esencialmente un requerimiento intangible en los juegos de pelea.</p>

				<h2>Wakeup</h2>

				<p>Los frames en los que un personaje esta levantándose del suelo.</p>

				<h2>Whiff</h2>

				<p>Un movimiento que falla completamente golpear al oponente.</p>

				<h2>Zoning</h2>

				<p>Zonear es una táctica en los juegos de pelea usualmente utilizada en rango medio, y el propósito es neutralizar los ataques del oponente, la idea es mantener el espacio para quedar en posición de responder o castigar cualquier intento de ataque de tu oponente.</p>
							
