Tier list según la opinión personal de Goro, Top player japones con Makoto, publicada el 13 de Abril del 2015:<br />
&nbsp;<br />
Goro (Makoto)&#8217;s Tier List<br />
&nbsp;<br />
S: Jin, Nu<br />
A: Valkenhayn, Hazama, Kokonoe, Relius, Litchi, Tager, Taokaka<br />
B: Azrael, Ragna, Tsubaki, Makoto, Bang, Izayoi, Carl, Noel, Kagura<br />
C: Rachel, Platinum, Bullet, Celica, Mu, Lambda<br />
D: Amane, Terumi, Hakumen, Arakune<br />
&nbsp;<br />
&#8220;Existe una diferencia en fuerza cuando comparas los A tier con los D&#8221;, pero no es tanta la diferencia cuando hablamos sobre los tier A hasta C. Ragna, considerado por muchos un personaje fuerte, fue colocado como tier B por la razón explicada abajo.</p>
<p>Sin importar que tan fuerte sea Ragna, el sigue siendo Ragna. Su &#8220;IAD Crossup 623D&#8221;, ciertamente es una herramienta muy fuerte para mixup, pero el resto de sus mixup no funcionan bien en contra de jugadores experimentados, que constantemente aparecen y obtienen buenos lugares en torneos. En otras palabras, es difícil no imaginarlo a el no teniendo un mal rato versus varios personajes cuando su mixup es mediocre y necesita confirmar muchas fintas en neutral&#8221;<br />
&nbsp;<br />
Tier list según la opinión personal de Dora, Top player japones con Bang, publicada el 13 de Abril del 2015:<br />
&nbsp;<br />
S: Hazama, Valkenhayn, Jin, Nu<br />
A: Kagura, Kokonoe, Azrael, Relius, Makoto, Litchi, Tager, Taokaka, Tsubaki, Ragna, Carl, Izayoi, Hakumen, Bang<br />
B: Noel, Rachel, Arakune, Platinum, Amane, Bullet, Mu, Celica, Lambda<br />
C+: Terumi<br />
&nbsp;<br />
&#8220;Los cuatro personajes que coloque como tier S, son los considerados extremadamente fuerte esta vez. A su vez, el juego parece estar bien balanceado &#8211; cuando comparamos los tier S y B, la diferencia en fuerza es visible, pero no es el caso cuando es entre los tier S y A. En general, ahora parece que, en su mayor parte, es mas importante considerar el match-up del personaje en vez de que tan fuerte el personaje es.&#8221;</p>
