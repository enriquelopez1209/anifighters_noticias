<div class="su-spoiler su-spoiler-style-fancy su-spoiler-icon-plus su-spoiler-closed"><div class="su-spoiler-title"><span class="su-spoiler-icon"></span>Razonamiento Tier list de eSport</div><div class="su-spoiler-content su-clearfix" style="display:none">
<span style="font-size: 16px;"><Strong>Nuevos personajes en XrdR (Johnny, Jack-O, Jam):</span></Strong></p>
<hr />
&nbsp;<br />
&#8211; Johnny, tier S.<br />
Debido a sus normales fuertes, alto daño en combos, okizeme fuertes y que generalmente sus malos matchup son parejos, incluso en los peores enfrentamientos en todos los ámbitos, es S tier.<br />
Posee hitboxes fuertes, largo alcance, y puede combinar a distancias donde el burst del oponente no lo golpearía, tiene imbloqueables y posee un mixup fuerte con zweihander. También gana tension rápidamente. </p>
<p>Ademas, puede realizar harto daño después de un air throw (antes de la versión 1.03). Probablemente Johnny se vuelva mas fuertes a medida que siga investigando mas sobre el.</p>
<p>-Jack-O, tier B.<br />
Jack-O es un personaje dificil de posicionar en ranking, debido a sus matchup polarisados. Dicho esto, recientemente, la investigación realizada en este personaje a hecho que varios matchup sean mejores, así que tenemos una buena opinión de ella.</p>
<p>Su &#8220;ganancia de tension automática&#8221; cerca de las calabazas es particularmente fuerte.<br />
Actualmente, su mayor problema es su inhabilidad de ganar en contra de su peor matchup, Axl.</p>
<p>&#8211; Jam, tentativamente tier C.</p>
<p>&#8220;Fundamentalmente bajo daño en combos&#8221;, &#8220;corto alcance&#8221;, &#8220;grandes hurtboxes&#8221;, &#8220;que no tenga proyectiles fuera de sus overdrives, su okizemi consiste de acercarse con saltos estándar, resultando que su afinidad con el Blitz Shield de Revelator sea mala&#8221; son algunos de los puntos débiles visibles que tiene. Actualmente, técnicas para mejorar sus debilidades no han sido encontradas, pero sobre todo, Jam posee una larga cantidad de malos matchup que la llevó a que obtuviese este ranking.<br />
&nbsp;<br />
&nbsp;<br />
<span style="font-size: 16px;"><Strong>Personajes de Xrd -SIGN- que posesionamos diferente de antes:</span></Strong></p>
<hr />
&nbsp;<br />
&#8211; Sin, tier S.</p>
<p>Sin tiene &#8220;normales fuertes, usando sus herramientas tales como beak diver, su fuerza en los poking es extremadamente alta, y cuando tu eres golpeado por uno de sus pokes, el daño que obtiene es muy alto&#8221;, &#8220;su okizeme es bastante fuerte y si el logra entrar, hace mucho daño&#8221; son las fuerzas de Sin y si tu piensas sobre sus mathchup, el no se desempeña mal en contra de Johnny, así que lo tratamos como S tier en vez de A tier.<br />
Sin embargo, esto no significa que no tenga malos matchup, su mayor desafió probablemente sea enfrentarse contra Bedmam.</p>
<p>&#8211; Elphelt, S tier</p>
<p>Este es un personaje que estabamos muy equivocados. Neutral fuerte, pokes fuertes, alto daño en combo, imbloqueables difíciles de manejar para el oponente, presión en esquina con escopeta, etc. hacen que ella sobresalga como personaje.<br />
Muchos jugadores dicen que &#8220;su neutral le gana a varios a varios personajes&#8221;, y &#8220;incluso si ella pierde en el neutral, no tiene ningún problema con esto gracias a la diferencia en daño&#8221;. Su buena afinidad con el Blitz Shield de Revelator también es una gran fuerza de ella.<br />
Actualmente, su reto es probablemente el match contra Johnny. Incluso Elphelt probablemente tiene dificultades contra Johnny.</p>
<p>&#8211; Potemkin, Tier B.<br />
&#8220;Difícil, pero alto daño en combos&#8221;, &#8220;debido a Trishula, alto daño en combo son mas estable, y el tiene (físicamente) okizeme que no se puede ver&#8221;, &#8220;posee buena afinidad con el Blitz Shield y Dust de Revelator&#8221;, por las razones anteriormente mencionadas, &#8220;el esta a un nivel donde no es el personaje mas débil&#8221;, así que lo posicionamos como tier B.<br />
Dicho esto, el es uno de los personajes mas débil dentro del tier B. El hecho que tenga dificultades en contra de muchos personajes, no a cambiando. Muchos jugadores piensan que el tiene dificultades en contra de Jack-O y Johnny (particularmente Johnny).<br />
Ademas, acordándonos  de versiones pasadas, nos preguntamos si Potemkin tendrá problema en contra de Jam, después de que ella reciba los buff.<br />
